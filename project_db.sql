-- Get total number of responses for a form
CREATE OR REPLACE FUNCTION fn_get_response_count(p_form_id INTEGER)
RETURNS INTEGER AS $$
DECLARE
    v_count INTEGER;
BEGIN
    SELECT COUNT(*) INTO v_count
    FROM responses
    WHERE form_id = p_form_id;
    RETURN v_count;
END;
$$ LANGUAGE plpgsql;

-- Check if a user can see a form based on access rules
-- Returns TRUE if the user's role/dept/batch matches the form's targeting
CREATE OR REPLACE FUNCTION fn_user_can_see_form(
    p_user_id   INTEGER,
    p_form_id   INTEGER
) RETURNS BOOLEAN AS $$
DECLARE
    v_user      RECORD;
    v_form      RECORD;
BEGIN
    SELECT role, dept_id, batch, institution_id
    INTO v_user FROM users WHERE user_id = p_user_id;

    SELECT access_type, target_dept_id, target_role, target_batch,
           institution_id, creator_id
    INTO v_form FROM forms WHERE form_id = p_form_id;

    -- Must be same institution
    IF v_user.institution_id != v_form.institution_id THEN
        RETURN FALSE;
    END IF;

    -- Admin and creator always see it
    IF v_user.role = 'admin' OR v_form.creator_id = p_user_id THEN
        RETURN TRUE;
    END IF;

    -- Check access type
    IF v_form.access_type = 'public' THEN
        RETURN TRUE;
    ELSIF v_form.access_type = 'dept' THEN
        RETURN v_user.dept_id = v_form.target_dept_id;
    ELSIF v_form.access_type = 'batch' THEN
        RETURN v_user.role = 'student'
           AND v_user.batch = v_form.target_batch;
    ELSIF v_form.access_type = 'role' THEN
        RETURN v_user.role = v_form.target_role;
    ELSIF v_form.access_type = 'dept_batch' THEN
        RETURN v_user.role = 'student'
           AND v_user.dept_id = v_form.target_dept_id
           AND v_user.batch = v_form.target_batch;
    ELSIF v_form.access_type = 'dept_role' THEN
        RETURN v_user.dept_id = v_form.target_dept_id
           AND v_user.role = v_form.target_role;
    END IF;

    RETURN FALSE;
END;
$$ LANGUAGE plpgsql;