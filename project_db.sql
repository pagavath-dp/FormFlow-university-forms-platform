-- 1. Prevent forms from being created with a past deadline
CREATE OR REPLACE FUNCTION fn_form_deadline_check()
RETURNS TRIGGER AS $$
BEGIN
    IF (TG_OP = 'INSERT') OR (TG_OP = 'UPDATE' AND NEW.deadline IS DISTINCT FROM OLD.deadline) THEN
        IF NEW.deadline <= NOW() THEN
            RAISE EXCEPTION 'Form deadline cannot be in the past.';
        END IF;
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE OR REPLACE TRIGGER trg_form_deadline_check
BEFORE INSERT OR UPDATE ON forms
FOR EACH ROW
EXECUTE FUNCTION fn_form_deadline_check();

-- 2. Prevent responses on closed or expired forms
CREATE OR REPLACE FUNCTION fn_prevent_invalid_response()
RETURNS TRIGGER AS $$
DECLARE
    v_status   VARCHAR(20);
    v_deadline TIMESTAMPTZ;
BEGIN
    SELECT status, deadline INTO v_status, v_deadline
    FROM forms
    WHERE form_id = NEW.form_id;

    IF v_status = 'closed' OR v_deadline < NOW() THEN
        RAISE EXCEPTION 'Cannot submit response: Form is closed or expired.';
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE OR REPLACE TRIGGER trg_prevent_invalid_response
BEFORE INSERT ON responses
FOR EACH ROW
EXECUTE FUNCTION fn_prevent_invalid_response();

-- 3. Audit form creation
CREATE OR REPLACE FUNCTION fn_audit_form_creation()
RETURNS TRIGGER AS $$
BEGIN
    INSERT INTO audit_logs (institution_id, action_type, user_id, form_id)
    SELECT u.institution_id, 'CREATED NEW FORM', NEW.creator_id, NEW.form_id
    FROM users u WHERE u.user_id = NEW.creator_id;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE OR REPLACE TRIGGER trg_audit_form_creation
AFTER INSERT ON forms
FOR EACH ROW
EXECUTE FUNCTION fn_audit_form_creation();

-- 4. Audit response submission
CREATE OR REPLACE FUNCTION fn_audit_response()
RETURNS TRIGGER AS $$
BEGIN
    INSERT INTO audit_logs (institution_id, action_type, user_id, form_id)
    SELECT u.institution_id, 'SUBMITTED RESPONSE', NEW.user_id, NEW.form_id
    FROM users u WHERE u.user_id = NEW.user_id;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE OR REPLACE TRIGGER trg_audit_response
AFTER INSERT ON responses
FOR EACH ROW
EXECUTE FUNCTION fn_audit_response();

-- 5. Audit member added by admin
CREATE OR REPLACE FUNCTION fn_audit_member_added()
RETURNS TRIGGER AS $$
BEGIN
    IF TG_OP = 'INSERT' AND NEW.institution_id IS NOT NULL THEN
        INSERT INTO audit_logs (institution_id, action_type, metadata)
        VALUES (NEW.institution_id, 'MEMBER ADDED', NEW.email);
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE OR REPLACE TRIGGER trg_audit_member_added
AFTER INSERT ON users
FOR EACH ROW
EXECUTE FUNCTION fn_audit_member_added();

