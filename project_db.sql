-- =========================
-- INSTITUTIONS
-- =========================
CREATE TABLE institutions (
    institution_id  INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name            VARCHAR(200) NOT NULL,
    domain          VARCHAR(100),               -- e.g. manipal.edu (optional, for display)
    created_at      TIMESTAMPTZ DEFAULT NOW()
);

-- =========================
-- DEPARTMENTS
-- =========================
CREATE TABLE departments (
    dept_id         INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    institution_id  INTEGER NOT NULL,
    name            VARCHAR(100) NOT NULL,
    CONSTRAINT fk_dept_institution
        FOREIGN KEY (institution_id)
        REFERENCES institutions(institution_id)
        ON DELETE CASCADE
);

-- =========================
-- USERS
-- =========================
-- role:   admin | hod | faculty | student
-- status: pending (added by admin, not signed up yet)
--         active  (signed up and ready)
CREATE TABLE users (
    user_id         INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    institution_id  INTEGER,                    -- NULL only for platform-level if needed
    dept_id         INTEGER,                    -- NULL for admin role
    name            VARCHAR(100),               -- filled on signup
    email           VARCHAR(200) UNIQUE NOT NULL,
    password        VARCHAR(255),               -- filled on signup (NULL = pending)
    role            VARCHAR(20) NOT NULL
        CHECK (role IN ('admin', 'hod', 'faculty', 'student')),
    batch           INTEGER,                    -- year of joining, for students
    status          VARCHAR(20) DEFAULT 'pending' NOT NULL
        CHECK (status IN ('pending', 'active')),
    joined_at       TIMESTAMPTZ DEFAULT NOW(),
    CONSTRAINT fk_user_institution
        FOREIGN KEY (institution_id)
        REFERENCES institutions(institution_id)
        ON DELETE CASCADE,
    CONSTRAINT fk_user_dept
        FOREIGN KEY (dept_id)
        REFERENCES departments(dept_id)
        ON DELETE SET NULL
);

-- =========================
-- FORMS
-- =========================
-- access_type values:
--   public      → everyone in institution
--   dept        → specific department
--   batch       → specific batch/year
--   role        → specific role (e.g. faculty only)
--   dept_batch  → e.g. CSE 2023 batch
--   dept_role   → e.g. CSE faculty only
CREATE TABLE forms (
    form_id         INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    institution_id  INTEGER NOT NULL,
    title           VARCHAR(150) NOT NULL,
    description     TEXT,
    creator_id      INTEGER NOT NULL,
    access_type     VARCHAR(20) NOT NULL
        CHECK (access_type IN ('public', 'dept', 'batch', 'role', 'dept_batch', 'dept_role')),
    target_dept_id  INTEGER,                    -- FK to departments
    target_role     VARCHAR(20)
        CHECK (target_role IN ('admin', 'hod', 'faculty', 'student') OR target_role IS NULL),
    target_batch    INTEGER,                    -- specific batch year
    deadline        TIMESTAMPTZ NOT NULL,
    status          VARCHAR(20) DEFAULT 'open' NOT NULL
        CHECK (status IN ('open', 'closed')),
    created_at      TIMESTAMPTZ DEFAULT NOW(),
    form_code       VARCHAR(20) NOT NULL UNIQUE,
    report_released VARCHAR(5) DEFAULT 'no'
        CHECK (report_released IN ('yes', 'no')),
    theme_color     VARCHAR(20) DEFAULT 'emerald',
    allow_multiple  VARCHAR(5) DEFAULT 'no'
        CHECK (allow_multiple IN ('yes', 'no')),
    CONSTRAINT fk_forms_creator
        FOREIGN KEY (creator_id)
        REFERENCES users(user_id)
        ON DELETE CASCADE,
    CONSTRAINT fk_forms_institution
        FOREIGN KEY (institution_id)
        REFERENCES institutions(institution_id)
        ON DELETE CASCADE,
    CONSTRAINT fk_forms_dept
        FOREIGN KEY (target_dept_id)
        REFERENCES departments(dept_id)
        ON DELETE SET NULL
);

-- =========================
-- AUDIT LOGS
-- =========================
CREATE TABLE audit_logs (
    log_id          INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    institution_id  INTEGER,
    action_type     VARCHAR(100) NOT NULL,
    user_id         INTEGER,
    form_id         INTEGER,
    metadata        TEXT,                       -- optional extra info (e.g. "imported 45 members")
    log_time        TIMESTAMPTZ DEFAULT NOW(),
    CONSTRAINT fk_audit_institution
        FOREIGN KEY (institution_id)
        REFERENCES institutions(institution_id)
        ON DELETE CASCADE,
    CONSTRAINT fk_audit_user
        FOREIGN KEY (user_id)
        REFERENCES users(user_id)
        ON DELETE SET NULL,
    CONSTRAINT fk_audit_form
        FOREIGN KEY (form_id)
        REFERENCES forms(form_id)
        ON DELETE SET NULL
);

-- =========================
-- QUESTIONS
-- =========================
CREATE TABLE questions (
    question_id     INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    form_id         INTEGER NOT NULL,
    question_text   TEXT NOT NULL,
    question_type   VARCHAR(20) NOT NULL
        CHECK (question_type IN ('text', 'textarea', 'number', 'email', 'date', 'mcq')),
    is_required     SMALLINT NOT NULL CHECK (is_required IN (0, 1)),
    created_at      TIMESTAMPTZ DEFAULT NOW(),
    CONSTRAINT fk_questions_form
        FOREIGN KEY (form_id)
        REFERENCES forms(form_id)
        ON DELETE CASCADE
);

-- =========================
-- OPTIONS
-- =========================
CREATE TABLE options (
    option_id       INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    question_id     INTEGER NOT NULL,
    option_text     TEXT NOT NULL,
    CONSTRAINT fk_options_question
        FOREIGN KEY (question_id)
        REFERENCES questions(question_id)
        ON DELETE CASCADE
);

-- =========================
-- RESPONSES
-- =========================
CREATE TABLE responses (
    response_id     INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    form_id         INTEGER NOT NULL,
    user_id         INTEGER NOT NULL,
    submitted_at    TIMESTAMPTZ DEFAULT NOW() NOT NULL,
    CONSTRAINT fk_responses_form
        FOREIGN KEY (form_id)
        REFERENCES forms(form_id)
        ON DELETE CASCADE,
    CONSTRAINT fk_responses_user
        FOREIGN KEY (user_id)
        REFERENCES users(user_id)
        ON DELETE CASCADE
);

-- =========================
-- ANSWERS
-- =========================
CREATE TABLE answers (
    answer_id           INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    response_id         INTEGER NOT NULL,
    question_id         INTEGER NOT NULL,
    answer_text         TEXT,
    answer_number       NUMERIC(10, 4),
    selected_option_id  INTEGER,
    CONSTRAINT fk_answers_response
        FOREIGN KEY (response_id)
        REFERENCES responses(response_id)
        ON DELETE CASCADE,
    CONSTRAINT fk_answers_question
        FOREIGN KEY (question_id)
        REFERENCES questions(question_id)
        ON DELETE CASCADE,
    CONSTRAINT fk_answers_option
        FOREIGN KEY (selected_option_id)
        REFERENCES options(option_id),
    CONSTRAINT chk_one_answer_only CHECK (
        (answer_text IS NOT NULL AND answer_number IS NULL AND selected_option_id IS NULL)
        OR
        (answer_text IS NULL AND answer_number IS NOT NULL AND selected_option_id IS NULL)
        OR
        (answer_text IS NULL AND answer_number IS NULL AND selected_option_id IS NOT NULL)
    )
);

